#include "main.h"
/**
 * print_square - f
 * @size: asd
 */
void print_square(int size)
{
	int i, j;
		for (i = 1; i <= size; i++)
		{
			if (size > 0)
			{
				for (j = 1; j <= size; j++)
				{
					if (size > 0)
					{
						_putchar('#');
					}
					{
						continue;
					}
				}
			}
			_putchar('\n');
		}
}
